class DiaagCalculator:

    def __init__(self, a, b):
        self.a = a
        self.b = b

    def sum(self):
        return self.a + self.b

    def difference(self):
        return self.a - self.b

    def product(self):
        return self.a * self.b

    def hello(self):
        print("Hello from the DIAAG team")


